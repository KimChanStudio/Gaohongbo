                <h4>@早年经历</h4>
                <p>
                    高洪波，回族，祖籍辽宁，1966年出生于北京市丰台区桥梁厂一个贫困的工人家庭。1976年，被著名教练徐根宝从丰台桥梁厂子弟小学足球队相中，进入崇文区体校。1981年就读汇文中学期间入选北京青年队。1983年随队获得全国青年联赛冠军，个人获最佳射手。<br>
                    1985年，高洪波成为中国青年队的主力前锋，在当年亚青赛上，率领中青队历史性夺冠，他个人包办了全队的5粒进球，荣获最佳球员。在随后举行的莫斯科世青赛上，淘汰英格兰队，进入8强，在1/4决赛憾负东道主苏联队。<br>
                    1985年，高洪波进入了北京一线队，1987年入选国家二队，在专业队时代多次获得联赛最佳射手。由于当时国家队主帅高丰文与徐根宝之间的隔阂，长期将高洪波排除在国家队大门之外。直到施拉普纳上任后，高洪波才得以入选国足，并成为队内头号射手。<br>
                    1994年开始后，高洪波远走新加坡，加盟中峇鲁选区体育俱乐部。1995年回归国安，并帮助球队夺得足协杯冠军，联赛亚军。1997年，高洪波以当年“标王”身份转会广州松日，同年获得最佳射手，帮助球队冲A成功。处于职业生涯末期的他在世预赛小组赛对阵越南的比赛中，完成了自己国脚生涯的告别演出。1999年，高洪波选择退役。<br>

                </p>
