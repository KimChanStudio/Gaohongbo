                <h4>@简介</h4>
                <p>
                    高洪波，1966年1月29日出生于北京市丰台区，祖籍辽宁，回族人，中国职业足球运动员、中国国家男子足球队主教练。<br>
                    球员时代曾是中国青年队主力前锋。1985年亚青赛决赛中率队夺冠，个人打进5球，并荣获最佳球员。1996年获得中国足协杯冠军。1998年获得中国足球甲A联赛冠军。1999年退役。<br>
                    2000年，高洪波成为中国国少队主教练。2001年加盟甲B球队上海中远队，任助理教练，辅佐徐根宝率队成功升入甲A。2004年接手厦门蓝狮队。2005年率领厦门蓝狮队获得甲A冠军并成功冲超。<br>
                    2006年底成为长春亚泰队主教练，并于上任的首个赛季率队获得中国足球超级联赛冠军，成为中国职业联赛历史上最年轻的冠军主帅。该赛季高洪波还当选为联赛最佳主教练。<br>
                    2009年成为中国国家足球队主教练。2011年率领中国队进入2014年巴西世界杯预选赛亚洲区二十强。同年9月成为陕西浐灞（贵州人和）队主教练。2013年2月成为上海东亚（上海上港）队主教练。2013年11月成为江苏舜天队主教练，2015年卸任。<br>
                    2015年9月24日，高洪波出席加盟海牙发布会，成中国本土教练赴欧执教首人。  2015年12月18日，高洪波正式成为中国足协执委会委员。  2016年2月3日，中国足协确认，高洪波将正式出任中国国家男子足球队主教练。<br>
                    2016年2月3日，中国足协官方网站确认，高洪波将接替此前下课的法国人佩兰，重新出任中国国家队主教练。<br>
                    2016年4月1日，中国足协官网确认，正式聘请国足代理主帅高洪波为中国男子国家队主教练，高洪波将带队征战12强赛！<br>

