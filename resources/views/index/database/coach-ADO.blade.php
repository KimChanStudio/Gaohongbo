        <h3 class="panel-title">在海牙开眼界收获大</h3>
    </div>
    <div class="panel-body">

        <p>去年9月底，高洪波赴荷兰海牙俱乐部以教练组成员的身份学习、考察、工作了两个多月。谈及此次前去海牙俱乐部的收获时，高洪波表示，因为海牙去年已被北京合力万盛国际体育发展有限公司收购，这次同以往不同，俱乐部对自己全面开放，让自己从队伍训练、比赛准备、青训、俱乐部管理和运营等多方面进行了考察和学习，获得了大量实战性强的知识，更开阔了眼界。</p>
        <p>“以往出国考察、学习，都是人家俱乐部帮助安排，安排谁？安排在什么时间段？交流到什么样的程度？能够考察哪个队的训练和比赛……许多方面相对都要被动些。</p>
        <p>“而这次去荷兰则不同，高效、针对性强，海牙俱乐部基本上都根据我此次出去的想法，安排了时间和希望要交流的教练，同时也安排了想要考察的训练内容和比赛的相关准备，交流和探讨的内容十分丰富，也很深入，真正获得了一些我想要的东西。”</p>
        <p>不过，高洪波也坦言，尽管自己很欣赏和喜爱荷兰足球的风格。但是近年来，荷兰足球整体上出现下滑，尤其是青训方面也出现了一些问题，后备人才方面不再像以往那样雄厚，投入的资金也并不太乐观。荷兰足球要想像以往那样很快就能重振雄风，估计有些难了。</p>
