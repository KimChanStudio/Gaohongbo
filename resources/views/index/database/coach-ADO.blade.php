    <div class="panel-body">
        <h3>离任江苏舜天 <small>将任荷甲海牙队助理教练</small></h3>
        <p>从江苏舜天俱乐部主教练位置上离任之后，高洪波最近一直在家里休息。今天记者从有关方面获得消息，作为国内中生代教练员的代表，高洪波已经于荷甲联赛的海牙俱乐部签订了一份为期一年的工作合同，担任海牙俱乐部的助理教练。目前高洪波正在家中等待荷兰签证。如果一切顺利，本周六高指导就将离开北京，飞赴荷兰，开始新赛季的工作。</p>
        <p>高洪波也因此将成为中国本土教练员执教外国球队的第一人，此前高洪波从国家队下课之后，曾经接到过塔吉克斯坦国家队的邀请，希望他能够担任塔吉克斯坦国家队的主教练，带队征战当年的20强赛，但是考虑到各方面的特殊情况，高洪波最后还是选择了放弃。</p>
        <p>
            不过此次能够前往荷兰执教，高洪波显得非常兴奋，在他看来，此次荷兰之行既是展示中国教练员水平的一个平台，也是近距离向欧洲高水平联赛学习的难得契机。“荷兰足球风格一直是我非常喜欢的，这也是我选择前往荷兰的原因，之前确实也有其他有中国资本介入的俱乐部找过我，想给我提供前往欧洲执教的机会，但最后还是选择了荷兰，一方面是这个原因，另外一方面，荷兰足球的青训也非常吸引我的。”
        </p>

