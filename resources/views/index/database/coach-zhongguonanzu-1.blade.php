        <h3>“不可能的任务” <small>两年时间没人能让国家队脱胎换骨</small></h3>
        <p>高洪波上任之后，对于刚刚经历了历史最低谷，又遭遇反赌打黑的中国足球来说。无论官员、球迷、媒体，最需要的是什么？成绩。只有成绩能给低谷中的中国足球信心，但这恰恰是这支中国队难以提供给大家的。高洪波所遭遇的环境期望值与中国足球本身的水平并不对等。</p>
        <p>所以我们看到的是这样一个结果：足协不断安排各种实力不济的热身赛，避免输球带来更大的负面效应。而球迷对这些含金量“不足”的比赛并不买账。最终的结果是国足也得不到真正的锻炼，从而在大赛上折戟沉沙。</p>
        <p>换句话来说，高洪波无形中承担的是挽救国足形象、带国足走出泥潭这样一项不可能的任务。但对于中国足球来说，谁带队也无法在这2年的时间内获得脱胎换骨的改变，更何况他还要遭遇成绩的压力，混乱的环境并且得不到真正的锻炼。</p>
    </div>
    <div class="panel-body">
        <h3>困难重重 <small>已经做得足够好</small></h3>
        <p>含蓄、自谦的中国人谈及庸人或自己，喜欢说“没有功劳，也有苦劳”。不过从放眼长远、培养新人，营造团结、清除球霸，提升排名、拿下种子队，以及助中国足协度过资金难关、为继任者赢得改造时间几个方面来说，少帅高洪波的国家队之旅虽然不尽完美，但仍可算无愧于“既有苦劳，也有功劳”评价。</p>

