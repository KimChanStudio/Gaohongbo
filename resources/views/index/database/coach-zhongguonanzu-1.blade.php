        <h3>反腐风暴 <small>四强赛期间遭遇外部质疑+内部整顿</small></h3>
        <p>高洪波上任五个月之后，浩浩荡荡的“反赌风暴”便拉开帷幕，在长达一年多的反赌风暴中，足协高官、俱乐部老板、球员、裁判纷纷落马，而在这段人人自保的时期，高洪波和他所在的国家队也成为舆论长期质疑的对象。</p>
        <p>“高洪波赴日签证被'拒约'，高洪波被'边控'”一度见诸各大媒体的头条。2010年1月，足协副主席南勇、杨一民落马，反假赌黑风暴由此达到高潮。而就在2月东亚四强赛开始之际，国内司法程序仍在继续，主教练高洪波、队长杜威等被社会舆论所怀疑，整个国足承载着巨大的压力。而这样的压力不仅来自外部，也来自足协内部。</p>
        <p>2010年3月10日，就在高洪波刚刚率领中国队在东亚四强赛夺冠后不久，由国家体育总局布置，中国足协副主席林晓华牵头的国字号教育整顿工作开始。从3月10日到3月12日，各级国家队远赴广东三水训练基地进行为期三天的整顿。由于此前一直有消息称高洪波等人有问题，这次国字号的整顿再次引起媒体的广泛关注。</p>
