        <h3>自助者天助 <small>国足完成惊天大逆转</small></h3>
        <p>在3月29日进行的世界杯预选赛亚洲区小组赛最后一轮，中国男足面对卡塔尔，只有获胜一条路才能让我们在小组第二的积分比拼时占优。</p>
        <p>最终凭借黄博文和武磊的进球，中国队2比0战胜卡塔尔。而由于约旦1比5不敌澳大利亚，朝鲜2比3不敌菲律宾，伊朗2比0战胜阿曼，中国队成功跻身四个成绩最好的小组第二名，晋级亚洲区12强赛。而中国男足上次参加该阶段比赛，还要追溯到2001年。</p>
        <p>亚洲区12强赛抽签将于4月12日举行，9月1日开始第一轮较量。小组各前两名可直接晋级2018年俄罗斯世界杯，随后两个小组的第三名将进行附加赛，胜者将与中北美及加勒比区的第四名争夺1个晋级名额。</p>
