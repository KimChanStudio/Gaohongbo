        <h3>遗憾！国足热身输球 <small>国足0-1十人哈萨克斯坦止连胜</small></h3>
        <p>国足19年后重回大连：1997年的10强赛，国足将主场设在了大连，此后国足的比赛从未安排在大连进行，这一次与哈萨克斯坦的热身赛，是国足19年后重新回到大连比赛。</p>
        <p>5名大连籍国脚首发+大轮换：本期集训名单中，一共有6名大连籍的国脚，与哈萨克斯坦一役，冯潇霆 、王大雷、杨旭、李学鹏和赵明剑 5名球员全部首发出战，于汉超 下半场也是替补出战。而且高洪波本场比赛也是对首发阵容进行了大面积调整，除了张琳芃、赵明剑和冯潇霆连续2场首发外，其余位置与对阵特立尼达和多巴哥的比赛时相比做出了8处调整。</p>
        <p>高家军止连胜：高洪波首次执掌国足教鞭时期，最后6场比赛全胜。这一次拾起国足教鞭后，又连胜马尔代夫、卡塔尔和特立尼达和多巴哥，但与哈萨克斯坦的比赛，高家军0-1落败，连胜势头遭止。</p>
