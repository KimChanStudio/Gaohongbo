        <h3>国足两平香港 出线堪忧 <small>2016.1.7 佩兰正式下课</small></h3>
        <p class="lead">首战6-0不丹留隐患 半场最后时刻方进球</p>
        <p>中国队在首轮比赛中轮空，作为C组的种子队，中国队在第二轮比赛中客场对阵不丹。不丹在世预赛首场比赛中客场0-7输给中国香港。这场比赛，如果从比分来看，国足可谓是砍瓜切菜，杨旭完成帽子戏法，于大宝2球，武磊也收获进球。但国足直到上半场补时阶段才进球，说明国足的进攻还是存在问题，后面对阵中国香港以及卡塔尔没能赢球，也印证了这一观点。</p>
        <p class="lead">次战主场0-0中国香港 4中框+对手逃点球</p>
        <p>这场在深圳举行的比赛，中国队占尽优势，郑智和于汉超在上半场比赛中各自击中两次门柱。最后时刻，中国香港队陈肇麒禁区内明显手球，裁判没有判罚。主场没能击败中国香港，是国足一步步走上不归路的开始。</p>
        <p class="lead">第3战3-0马尔代夫 仅赢3球太失望</p>
        <p>中国队这一场原本是客场作战，不过马尔代夫因为场地条件放弃了主场，改在沈阳进行。国足开赛第8分钟即由于大宝进球，但接下来直到第57分钟才再次进球，张琳芃第66分钟进球，国足仅仅以3-0的比分取胜。值得注意的是，马尔代夫在此战中全部由业余球员组成，面对这样的球队，国足竟然只打进3球，实在让人失望。</p>
        <p class="lead">第4战客场0-1卡塔尔 国足降至第三</p>
        <p>国足客场对阵卡塔尔，布迪亚夫在第22分钟打进全场唯一进球。0-1输球的中国队排名滑落到第三的位置，争夺小组头名从而确保出线权的前景已非常被动。</p>
        <p class="lead">第5战12-0屠杀不丹 国足再次看到希望</p>
        <p>主场对阵不丹，杨旭打入4球，于汉超、于大宝与王永珀均梅开二度，梅方与张稀哲也取得入球。国足在少赛1场的情况下积10分，而多赛1场的中国香港积13分，国足只要在17号的比赛中击败中国香港就可以升至第二的位置。当时，不少球迷认为国足完全可以在客场击败对手，最终争取拿到“四个成绩最好的小组第二”的成绩。</p>
        <p class="lead">第6战客场0-0中国香港 国足几被判死缓</p>
        <p>今晚和中国香港队的比赛中，国足再次4次击中门柱，此外于大宝的进球被裁判忽视，国足最终0-0战平。6场比赛下来，国足3胜2平1分积11分。国足最后两轮比赛即便拿到两连胜，也很难成为“四个成绩最好的小组第二”中的一支。可以说，今晚过后，国足已经基本被判了死缓。</p>
