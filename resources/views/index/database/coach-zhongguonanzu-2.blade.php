    <div class="panel-body">
        <h3>19岁张玉宁创国足里程碑 <small>中国男足以4比2大胜特立尼达和多巴哥队</small></h3>
        <p>6月3日，在秦皇岛主场，中国男足以4比2大胜来自中北美及加勒比地区的劲旅特立尼达和多巴哥队，在这场练兵重于结果的热身赛中，取胜并不是中国队的主要追求。</p>
        <p>效力于荷甲的19岁小将张玉宁首度代表国足出战就梅开二度并助攻另一位新人队友胡人天锦上添花，而他们和场上其他新人纷纷交出了不错的成绩单，在这个夜晚，国足主帅高洪波发现了不少优质“板凳存货”，在漫漫12强赛征途中，这些新生力量是国足冲击强大对手的有益补充，而新老力量相互勉励、相互促进，将为国足冲击俄罗斯世界杯提供了更为丰富的战术选择。</p>
        <p>和15年前找特立尼达和多巴哥热身，为10强赛备战一样，这一次的特立尼达和多巴哥则被国足当成12强赛中对手伊朗的“翻版”。同样是备战世预赛亚洲区的最后阶段比赛，上一次国足取胜这支中北美洲劲旅后在10强赛中顺风顺水，这一次3个月后开打的12强赛，国足能将历史复制一次吗？</p>
